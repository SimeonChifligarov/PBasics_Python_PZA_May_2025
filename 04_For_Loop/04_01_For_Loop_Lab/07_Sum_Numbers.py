# Да се напише програма, която чете n-на брой цели числа, въведени от потребителя
# и ги сумира.
# •	От първия ред на входа се въвежда броят числа n.
# •	От следващите n реда се въвежда по едно цяло число.
# Програмата трябва да прочете числата, да ги сумира и да отпечата сумата им.

num_count = int(input())
total_sum = 0

for _ in range(num_count):  # range(2):  [0, 1]
    number = int(input())
    total_sum += number

print(total_sum)
